#ifndef StringReader_HPP
#define StringReader_HPP

#include <stdio.h>
#include <stdlib.h>

#include "ReaderImpl.hpp"

template<class T> class StringReader:public ReaderImpl
{
private:
    const T *str;
    int readPos;
    int strLength;
public:
    StringReader(const T *a,int ln):ReaderImpl()
    {
        str=a;
        strLength=ln;
        readPos=0;
    }
    int getNextChar()
    {
        if(readPos<strLength)
        {
            currentChar=str[readPos];
            readPos++;
            return 1;
        }
        return 0;
    }
};

#endif
